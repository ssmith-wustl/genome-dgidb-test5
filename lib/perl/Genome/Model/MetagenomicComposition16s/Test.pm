sub _amplicons {
    my $self = shift;

    unless ( $self->{_amplicons} ) {
        $self->{_amplicons} = [];
        my ($amplicon_set) = $self->_build->amplicon_sets
            or die "No amplicons found";
        while ( my $amplicon = $amplicon_set->next_amplicon ) {
            push @{$self->{_amplicons}}, $amplicon;
        }
    }

    return $self->{_amplicons};
}

